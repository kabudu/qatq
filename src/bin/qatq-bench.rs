fn parse_manifest_value(value: &str) -> String {
    value
        .strip_prefix('"')
        .and_then(|stripped| stripped.strip_suffix('"'))
        .unwrap_or(value)
        .trim()
        .to_string()
}

#[derive(Default)]
struct ManifestFixture {
    group: Option<String>,
    name: Option<String>,
    path: Option<PathBuf>,
    shape: Option<String>,
    notes: Option<String>,
}

impl ManifestFixture {
    fn set(
        &mut self,
        key: &str,
        value: String,
        path: &Path,
        line_number: usize,
    ) -> Result<(), String> {
        match key {
            "group" => self.group = Some(value),
            "name" => self.name = Some(value),
            "path" => self.path = Some(PathBuf::from(value)),
            "shape" => self.shape = Some(value),
            "notes" => self.notes = Some(value),
            other => {
                return Err(format!(
                    "{}:{line_number}: unsupported fixture key {other}",
                    path.display()
                ));
            }
        }
        Ok(())
    }

    fn into_fixture_input(
        self,
        base_dir: &Path,
        manifest_path: &Path,
        line_number: usize,
    ) -> Result<FixtureInput, String> {
        let group = self.group.unwrap_or_else(|| "fixture".to_string());
        let name = self.name.ok_or_else(|| {
            format!(
                "{}:{line_number}: fixture is missing required name",
                manifest_path.display()
            )
        })?;
        let path = self.path.ok_or_else(|| {
            format!(
                "{}:{line_number}: fixture is missing required path",
                manifest_path.display()
            )
        })?;
        let path = if path.is_absolute() {
            path
        } else {
            base_dir.join(path)
        };
        Ok(FixtureInput {
            group,
            name,
            path,
            shape: self.shape,
            notes: self.notes,
        })
    }
}

fn encode_raw_f32le(values: &[f32]) -> Vec<u8> {
    let mut out = Vec::with_capacity(values.len() * 4);
    for value in values {
        out.extend_from_slice(&value.to_le_bytes());
    }
    out
}

fn decode_raw_f32le(bytes: &[u8]) -> Result<Vec<f32>, String> {
    if bytes.len() % 4 != 0 {
        return Err("raw f32le byte length is not divisible by 4".to_string());
    }
    Ok(bytes
        .chunks_exact(4)
        .map(|chunk| f32::from_le_bytes(chunk.try_into().expect("chunk size checked")))
        .collect())
}

fn encode_fp8_e4m3(values: &[f32]) -> Vec<u8> {
    values.iter().copied().map(encode_fp8_e4m3_value).collect()
}

fn decode_fp8_e4m3(bytes: &[u8]) -> Vec<f32> {
    bytes.iter().copied().map(decode_fp8_e4m3_value).collect()
}

fn encode_fp8_e4m3_value(value: f32) -> u8 {
    if value == 0.0 || !value.is_finite() {
        return 0;
    }
    let sign = if value.is_sign_negative() { 0x80 } else { 0x00 };
    let abs = value.abs().clamp(2.0_f32.powi(-6), 240.0);
    let exponent = abs.log2().floor() as i32;
    let exponent = exponent.clamp(-6, 7);
    let base = 2.0_f32.powi(exponent);
    let mantissa = ((abs / base - 1.0) * 8.0).round().clamp(0.0, 7.0) as u8;
    let biased = ((exponent + 7) as u8) & 0x0f;
    sign | (biased << 3) | mantissa
}

fn decode_fp8_e4m3_value(byte: u8) -> f32 {
    if byte == 0 {
        return 0.0;
    }
    let sign = if byte & 0x80 == 0 { 1.0 } else { -1.0 };
    let exponent = (((byte >> 3) & 0x0f) as i32) - 7;
    let mantissa = (byte & 0x07) as f32 / 8.0;
    sign * (1.0 + mantissa) * 2.0_f32.powi(exponent)
}

fn error_metrics(before: &[f32], after: &[f32]) -> (f32, f32) {
    let mut max_abs = 0.0_f32;
    let mut sum_squared = 0.0_f64;
    for (before, after) in before.iter().zip(after.iter()) {
        let error = before - after;
        max_abs = max_abs.max(error.abs());
        sum_squared += (error as f64) * (error as f64);
    }
    let rmse = if before.is_empty() {
        0.0
    } else {
        (sum_squared / before.len() as f64).sqrt() as f32
    };
    (max_abs, rmse)
}

fn exact_f32_bits(before: &[f32], after: &[f32]) -> bool {
    before.len() == after.len()
        && before
            .iter()
            .zip(after.iter())
            .all(|(before, after)| before.to_bits() == after.to_bits())
}
