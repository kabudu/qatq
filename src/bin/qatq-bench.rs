use std::{
    env, fs,
    hint::black_box,
    io::{BufReader, BufWriter, Read, Write},
    path::{Path, PathBuf},
    time::{Duration, Instant},
};

use qatq::{
    compression_ratio, decode, decode_lossy_i4, decode_phase1_q4, encode_lossless_f32,
    encode_lossy_i4, encode_phase1_q4, encode_phase2_lossless, encode_phase2_lossless_container,
    encode_phase2_lossless_exhaustive, phase2_lossless_strategy,
};

const ITERATIONS: usize = 200;
const TIMING_SAMPLES: usize = 3;
const CONTAINER_CHUNK_VALUES: usize = 65_536;

fn main() {
    if let Err(error) = run() {
        eprintln!("qatq-bench: {error}");
        std::process::exit(1);
    }
}

fn run() -> Result<(), String> {
    let options = parse_options()?;
    let mut dataset_specs = Vec::new();
    if options.include_synthetic {
        dataset_specs.extend([
            dataset_smooth_sine(16_384),
            dataset_kv_like(16_384),
            dataset_spiky(16_384),
            dataset_bit_delta(16_384),
        ]);
    }
    dataset_specs.extend(options.inputs.iter().cloned().map(DatasetSpec::External));
    for manifest_path in &options.manifests {
        dataset_specs.extend(
            read_manifest_fixtures(manifest_path)?
                .into_iter()
                .map(DatasetSpec::External),
        );
    }
    if dataset_specs.is_empty() {
        return Err("no benchmark datasets selected".to_string());
    }
    preflight_external_datasets(&dataset_specs)?;

    let mut benchmarked = Vec::with_capacity(dataset_specs.len());
    for dataset_spec in dataset_specs {
        let dataset = load_dataset(dataset_spec)?;
        let summary = dataset.summary();
        let results = benchmark_dataset(&dataset)?;
        benchmarked.push(BenchmarkedDataset { summary, results });
    }

    let mut report = String::new();
    report.push_str("# Phase 1 Benchmarks\n\n");
    report.push_str(
        "Generated by `cargo run --release --bin qatq-bench -- --output docs/BENCHMARKS.md`.\n\n",
    );
    report.push_str("These are deterministic microbenchmarks for codec-level comparison. Synthetic datasets are included by default. External raw `f32le` fixtures can be added with `--input name:path.f32le` or fixture manifests.\n\n");
    report.push_str(&format!("- iterations per timing sample: `{ITERATIONS}`\n"));
    report.push_str(&format!("- timing samples: `{TIMING_SAMPLES}`\n"));
    report.push_str("- timing value: `best sample mean`\n");
    report.push_str(&format!(
        "- external fixtures: `{}`\n",
        count_external_fixtures(&benchmarked)
    ));
    report.push_str(&format!(
        "- fixture manifests: `{}`\n",
        options.manifests.len()
    ));
    report.push_str(&format!(
        "- synthetic controls: `{}`\n",
        if options.include_synthetic {
            "enabled"
        } else {
            "disabled"
        }
    ));
    report.push_str(&format!(
        "- target: `{}` / `{}`\n\n",
        env::consts::OS,
        env::consts::ARCH
    ));
    report.push_str("| group | dataset | values | codec | phase2 strategy | encoded bytes | ratio vs raw f32 | encode us | decode us | exact bits | max abs error | RMSE |\n");
    report.push_str(
        "| --- | --- | ---: | ---: | --- | ---: | ---: | ---: | ---: | --- | ---: | ---: |\n",
    );

    for benchmarked_dataset in &benchmarked {
        for result in &benchmarked_dataset.results {
            report.push_str(&format!(
                "| {} | {} | {} | {} | {} | {} | {:.4} | {:.2} | {:.2} | {} | {:.6} | {:.6} |\n",
                benchmarked_dataset.summary.group,
                benchmarked_dataset.summary.name,
                benchmarked_dataset.summary.value_count,
                result.codec,
                result.phase2_strategy.unwrap_or(""),
                result.encoded_len,
                result.ratio,
                result.encode_us,
                result.decode_us,
                if result.exact_bits { "yes" } else { "no" },
                result.max_abs_error,
                result.rmse
            ));
        }
    }

    report.push_str("\n## Interpretation\n\n");
    report.push_str(
        "- `raw-f32le` is a transport-size and copy-cost control, not a compression codec.\n",
    );
    report.push_str("- `fp8-e4m3` is a local finite-value software baseline used for directional comparison until hardware/runtime FP8 paths are added.\n");
    report.push_str("- `lossy-i4` is the original PermeantOS seed baseline.\n");
    report.push_str("- `phase1-q4` is the new training-free quaternion rotation plus scalar q4 quantization path with a compact 1-bit residual-sign side channel.\n");
    report.push_str("- `phase2-lossless` is the default fast exact path: it accepts compression-positive byte-RLE or byte-plane RLE first, then probes adjacent-bit delta-XOR byte-plane RLE before building the more expensive Phase 1 predictor.\n");
    report.push_str("- `phase2-lossless-exhaustive` runs the deeper exact strategy search and is included to measure the latency/size tradeoff.\n");
    report.push_str("- `phase2-lossless-container` wraps exact Phase 2 payloads in the sequential `QATC` large-tensor file container.\n");
    report.push_str("- Phase 1 is still lossy. The residual side channel is an experiment for lowering reconstruction error, not a bit-identical lossless design.\n");
    report.push_str("- Real PermeantOS/KV-cache results should be added as external fixtures and kept separate from synthetic rows in paper tables.\n");
    report.push_str("- External fixture values are loaded and benchmarked one dataset at a time; reports keep only metadata and result rows so multiple large captures do not remain resident together.\n");

    if let Some(path) = options.paper_output.as_ref() {
        write_text_atomic(path, &render_paper_summary(&benchmarked))?;
    }
    if let Some(path) = options.gate_output.as_ref() {
        let gate = evaluate_gate(&benchmarked, &options);
        write_text_atomic(path, &gate.report)?;
        if !gate.passed {
            return Err(format!("benchmark gate failed; see {}", path.display()));
        }
    }

    if let Some(path) = options.output {
        write_text_atomic(&path, &report)?;
    } else {
        print!("{report}");
    }
    Ok(())
}

fn parse_options() -> Result<BenchOptions, String> {
    let args: Vec<String> = env::args().collect();
    let mut output = None;
    let mut paper_output = None;
    let mut gate_output = None;
    let mut gate_require_external = false;
    let mut include_synthetic = true;
    let mut max_phase2_ratio = None;
    let mut max_phase2_encode_us = None;
    let mut max_phase2_decode_us = None;
    let mut max_phase2_container_ratio = None;
    let mut max_phase2_container_decode_us = None;
    let mut inputs = Vec::new();
    let mut manifests = Vec::new();
    let mut index = 1;
    while index < args.len() {
        match args[index].as_str() {
            "--output" => {
                index += 1;
                let path = args
                    .get(index)
                    .ok_or_else(|| "--output requires a path".to_string())?;
                output = Some(PathBuf::from(path));
            }
            "--paper-output" => {
                index += 1;
                let path = args
                    .get(index)
                    .ok_or_else(|| "--paper-output requires a path".to_string())?;
                paper_output = Some(PathBuf::from(path));
            }
            "--gate-output" => {
                index += 1;
                let path = args
                    .get(index)
                    .ok_or_else(|| "--gate-output requires a path".to_string())?;
                gate_output = Some(PathBuf::from(path));
            }
            "--gate-require-external" => {
                gate_require_external = true;
            }
            "--no-synthetic" => {
                include_synthetic = false;
            }
            "--max-phase2-ratio" => {
                index += 1;
                max_phase2_ratio = Some(parse_f64_arg(args.get(index), "--max-phase2-ratio")?);
            }
            "--max-phase2-encode-us" => {
                index += 1;
                max_phase2_encode_us =
                    Some(parse_f64_arg(args.get(index), "--max-phase2-encode-us")?);
            }
            "--max-phase2-decode-us" => {
                index += 1;
                max_phase2_decode_us =
                    Some(parse_f64_arg(args.get(index), "--max-phase2-decode-us")?);
            }
            "--max-phase2-container-ratio" => {
                index += 1;
                max_phase2_container_ratio = Some(parse_f64_arg(
                    args.get(index),
                    "--max-phase2-container-ratio",
                )?);
            }
            "--max-phase2-container-decode-us" => {
                index += 1;
                max_phase2_container_decode_us = Some(parse_f64_arg(
                    args.get(index),
                    "--max-phase2-container-decode-us",
                )?);
            }
            "--input" => {
                index += 1;
                let spec = args
                    .get(index)
                    .ok_or_else(|| "--input requires name:path.f32le".to_string())?;
                inputs.push(parse_fixture_input(spec)?);
            }
            "--manifest" => {
                index += 1;
                let path = args
                    .get(index)
                    .ok_or_else(|| "--manifest requires a path".to_string())?;
                manifests.push(PathBuf::from(path));
            }
            "--help" | "-h" => return Err(usage()),
            other => return Err(format!("unknown argument {other}\n{}", usage())),
        }
        index += 1;
    }
    Ok(BenchOptions {
        output,
        paper_output,
        gate_output,
        gate_require_external,
        include_synthetic,
        max_phase2_ratio,
        max_phase2_encode_us,
        max_phase2_decode_us,
        max_phase2_container_ratio,
        max_phase2_container_decode_us,
        inputs,
        manifests,
    })
}

fn parse_f64_arg(value: Option<&String>, name: &str) -> Result<f64, String> {
    let value = value.ok_or_else(|| format!("{name} requires a numeric value"))?;
    let parsed = value
        .parse::<f64>()
        .map_err(|error| format!("invalid {name} value {value}: {error}"))?;
    if !parsed.is_finite() || parsed < 0.0 {
        return Err(format!("{name} must be finite and non-negative"));
    }
    Ok(parsed)
}

fn parse_fixture_input(spec: &str) -> Result<FixtureInput, String> {
    let (name, path) = spec
        .split_once(':')
        .ok_or_else(|| format!("invalid fixture {spec}: expected name:path.f32le"))?;
    if name.trim().is_empty() {
        return Err(format!("invalid fixture {spec}: fixture name is empty"));
    }
    if path.trim().is_empty() {
        return Err(format!("invalid fixture {spec}: fixture path is empty"));
    }
    Ok(FixtureInput {
        group: "fixture".to_string(),
        name: name.to_string(),
        path: PathBuf::from(path),
        shape: None,
        notes: None,
    })
}

fn usage() -> String {
    "usage: qatq-bench [--output <path>] [--paper-output <path>] [--gate-output <path>] [--gate-require-external] [--no-synthetic] [--max-phase2-ratio <f64>] [--max-phase2-encode-us <f64>] [--max-phase2-decode-us <f64>] [--max-phase2-container-ratio <f64>] [--max-phase2-container-decode-us <f64>] [--input <name>:<path.f32le>]... [--manifest <path>]...".to_string()
}

struct BenchOptions {
    output: Option<PathBuf>,
    paper_output: Option<PathBuf>,
    gate_output: Option<PathBuf>,
    gate_require_external: bool,
    include_synthetic: bool,
    max_phase2_ratio: Option<f64>,
    max_phase2_encode_us: Option<f64>,
    max_phase2_decode_us: Option<f64>,
    max_phase2_container_ratio: Option<f64>,
    max_phase2_container_decode_us: Option<f64>,
    inputs: Vec<FixtureInput>,
    manifests: Vec<PathBuf>,
}

#[derive(Clone)]
struct FixtureInput {
    group: String,
    name: String,
    path: PathBuf,
    shape: Option<String>,
    notes: Option<String>,
}

#[derive(Clone)]
enum DatasetSpec {
    Synthetic {
        group: &'static str,
        name: &'static str,
        values: Vec<f32>,
        shape: Option<String>,
        notes: Option<String>,
    },
    External(FixtureInput),
}

fn load_dataset(spec: DatasetSpec) -> Result<Dataset, String> {
    match spec {
        DatasetSpec::Synthetic {
            group,
            name,
            values,
            shape,
            notes,
        } => Ok(Dataset {
            group: group.to_string(),
            name: name.to_string(),
            source: DatasetSource::Synthetic,
            values,
            shape,
            notes,
        }),
        DatasetSpec::External(fixture) => read_fixture_dataset(&fixture),
    }
}

fn preflight_external_datasets(specs: &[DatasetSpec]) -> Result<(), String> {
    for spec in specs {
        let DatasetSpec::External(fixture) = spec else {
            continue;
        };
        validate_f32le_file_metadata(&fixture.path)?;
    }
    Ok(())
}

fn benchmark_dataset(dataset: &Dataset) -> Result<Vec<BenchResult>, String> {
    let raw_encoded = encode_raw_f32le(&dataset.values);
    let raw_decoded = decode_raw_f32le(&raw_encoded)?;
    let fp8_encoded = encode_fp8_e4m3(&dataset.values);
    let fp8_decoded = decode_fp8_e4m3(&fp8_encoded);
    let lossy_encoded = encode_lossy_i4(&dataset.values);
    let lossy_decoded = decode_lossy_i4(&lossy_encoded).map_err(|error| error.to_string())?;
    let lossless_encoded = encode_lossless_f32(&dataset.values);
    let lossless_decoded = decode(&lossless_encoded).map_err(|error| error.to_string())?;
    let phase1_encoded = encode_phase1_q4(&dataset.values);
    let phase1_decoded = decode_phase1_q4(&phase1_encoded).map_err(|error| error.to_string())?;
    let phase2_encoded = encode_phase2_lossless(&dataset.values);
    let phase2_decoded = decode(&phase2_encoded).map_err(|error| error.to_string())?;
    let phase2_exhaustive_encoded = encode_phase2_lossless_exhaustive(&dataset.values);
    let phase2_exhaustive_decoded =
        decode(&phase2_exhaustive_encoded).map_err(|error| error.to_string())?;
    let phase2_container_encoded =
        encode_phase2_lossless_container(&dataset.values, CONTAINER_CHUNK_VALUES)
            .map_err(|error| error.to_string())?;
    let phase2_container_decoded =
        decode(&phase2_container_encoded).map_err(|error| error.to_string())?;

    let mut results = Vec::new();
    results.push(BenchResult::new(
        "raw-f32le",
        raw_encoded.len(),
        dataset.values.len(),
        None,
        time_encode(|| encode_raw_f32le(&dataset.values)),
        time_decode(|| decode_raw_f32le(&raw_encoded).expect("raw decode")),
        &dataset.values,
        &raw_decoded,
    ));
    results.push(BenchResult::new(
        "lossless-f32",
        lossless_encoded.len(),
        dataset.values.len(),
        None,
        time_encode(|| encode_lossless_f32(&dataset.values)),
        time_decode(|| decode(&lossless_encoded).expect("lossless decode")),
        &dataset.values,
        &lossless_decoded,
    ));
    results.push(BenchResult::new(
        "fp8-e4m3",
        fp8_encoded.len(),
        dataset.values.len(),
        None,
        time_encode(|| encode_fp8_e4m3(&dataset.values)),
        time_decode(|| decode_fp8_e4m3(&fp8_encoded)),
        &dataset.values,
        &fp8_decoded,
    ));
    results.push(BenchResult::new(
        "lossy-i4",
        lossy_encoded.len(),
        dataset.values.len(),
        None,
        time_encode(|| encode_lossy_i4(&dataset.values)),
        time_decode(|| decode_lossy_i4(&lossy_encoded).expect("lossy decode")),
        &dataset.values,
        &lossy_decoded,
    ));
    results.push(BenchResult::new(
        "phase1-q4",
        phase1_encoded.len(),
        dataset.values.len(),
        None,
        time_encode(|| encode_phase1_q4(&dataset.values)),
        time_decode(|| decode_phase1_q4(&phase1_encoded).expect("phase1 decode")),
        &dataset.values,
        &phase1_decoded,
    ));
    results.push(BenchResult::new(
        "phase2-lossless",
        phase2_encoded.len(),
        dataset.values.len(),
        phase2_strategy_label(&phase2_encoded),
        time_encode(|| encode_phase2_lossless(&dataset.values)),
        time_decode(|| decode(&phase2_encoded).expect("phase2 decode")),
        &dataset.values,
        &phase2_decoded,
    ));
    results.push(BenchResult::new(
        "phase2-lossless-exhaustive",
        phase2_exhaustive_encoded.len(),
        dataset.values.len(),
        phase2_strategy_label(&phase2_exhaustive_encoded),
        time_encode(|| encode_phase2_lossless_exhaustive(&dataset.values)),
        time_decode(|| decode(&phase2_exhaustive_encoded).expect("phase2 exhaustive decode")),
        &dataset.values,
        &phase2_exhaustive_decoded,
    ));
    results.push(BenchResult::new(
        "phase2-lossless-container",
        phase2_container_encoded.len(),
        dataset.values.len(),
        Some("qatc-container"),
        time_encode(|| {
            encode_phase2_lossless_container(&dataset.values, CONTAINER_CHUNK_VALUES)
                .expect("phase2 container encode")
        }),
        time_decode(|| decode(&phase2_container_encoded).expect("phase2 container decode")),
        &dataset.values,
        &phase2_container_decoded,
    ));
    Ok(results)
}

fn phase2_strategy_label(payload: &[u8]) -> Option<&'static str> {
    phase2_lossless_strategy(payload)
        .ok()
        .map(|strategy| strategy.as_str())
}

fn time_encode<T>(mut f: impl FnMut() -> T) -> f64 {
    time_loop(|| {
        black_box(f());
    })
}

fn time_decode<T>(mut f: impl FnMut() -> T) -> f64 {
    time_loop(|| {
        black_box(f());
    })
}

fn time_loop(mut f: impl FnMut()) -> f64 {
    let mut best = f64::INFINITY;
    for _ in 0..TIMING_SAMPLES {
        let started = Instant::now();
        for _ in 0..ITERATIONS {
            f();
        }
        best = best.min(duration_to_us(started.elapsed()) / ITERATIONS as f64);
    }
    best
}

fn duration_to_us(duration: Duration) -> f64 {
    duration.as_secs_f64() * 1_000_000.0
}

struct Dataset {
    group: String,
    name: String,
    source: DatasetSource,
    values: Vec<f32>,
    shape: Option<String>,
    notes: Option<String>,
}

impl Dataset {
    fn summary(&self) -> DatasetSummary {
        DatasetSummary {
            group: self.group.clone(),
            name: self.name.clone(),
            source: self.source,
            value_count: self.values.len(),
            shape: self.shape.clone(),
            notes: self.notes.clone(),
        }
    }
}

struct DatasetSummary {
    group: String,
    name: String,
    source: DatasetSource,
    value_count: usize,
    shape: Option<String>,
    notes: Option<String>,
}

struct BenchmarkedDataset {
    summary: DatasetSummary,
    results: Vec<BenchResult>,
}

#[derive(Clone, Copy, Debug, Eq, PartialEq)]
enum DatasetSource {
    Synthetic,
    External,
}

#[derive(Clone, Debug)]
struct BenchResult {
    codec: &'static str,
    phase2_strategy: Option<&'static str>,
    encoded_len: usize,
    ratio: f64,
    encode_us: f64,
    decode_us: f64,
    exact_bits: bool,
    max_abs_error: f32,
    rmse: f32,
}

impl BenchResult {
    fn new(
        codec: &'static str,
        encoded_len: usize,
        value_count: usize,
        phase2_strategy: Option<&'static str>,
        encode_us: f64,
        decode_us: f64,
        before: &[f32],
        after: &[f32],
    ) -> Self {
        let (max_abs_error, rmse) = error_metrics(before, after);
        let exact_bits = exact_f32_bits(before, after);
        Self {
            codec,
            phase2_strategy,
            encoded_len,
            ratio: compression_ratio(encoded_len, value_count),
            encode_us,
            decode_us,
            exact_bits,
            max_abs_error,
            rmse,
        }
    }
}

fn dataset_smooth_sine(count: usize) -> DatasetSpec {
    DatasetSpec::Synthetic {
        group: "synthetic",
        name: "smooth-sine",
        values: (0..count)
            .map(|index| {
                let x = index as f32;
                (x * 0.03125).sin() * 2.5 + (x * 0.0078125).cos() * 0.75
            })
            .collect(),
        shape: Some(format!("[{count}]")),
        notes: Some("smooth periodic control tensor".to_string()),
    }
}

fn dataset_kv_like(count: usize) -> DatasetSpec {
    DatasetSpec::Synthetic {
        group: "synthetic",
        name: "kv-like",
        values: (0..count)
            .map(|index| {
                let row = (index / 128) as f32;
                let col = (index % 128) as f32;
                let envelope = 1.0 / (1.0 + row * 0.0009);
                let local = (col * 0.113).sin() * 0.9 + (col * 0.017 + row * 0.003).cos() * 0.4;
                let drift = (row * 0.071).sin() * 0.2;
                (local + drift) * envelope
            })
            .collect(),
        shape: Some(format!("[{}, 128]", count / 128)),
        notes: Some("synthetic KV-like local correlation".to_string()),
    }
}

fn dataset_spiky(count: usize) -> DatasetSpec {
    DatasetSpec::Synthetic {
        group: "synthetic",
        name: "spiky",
        values: (0..count)
            .map(|index| {
                let x = index as f32;
                let base = (x * 0.041).sin() * 0.8;
                let spike = if index % 251 == 0 {
                    7.0
                } else if index % 113 == 0 {
                    -5.5
                } else {
                    0.0
                };
                base + spike
            })
            .collect(),
        shape: Some(format!("[{count}]")),
        notes: Some("synthetic outlier stress tensor".to_string()),
    }
}

fn dataset_bit_delta(count: usize) -> DatasetSpec {
    let mut bits = 0x3f00_0001_u32;
    DatasetSpec::Synthetic {
        group: "synthetic",
        name: "bit-delta",
        values: (0..count)
            .map(|_| {
                bits ^= 0x0102_0304;
                f32::from_bits(bits)
            })
            .collect(),
        shape: Some(format!("[{count}]")),
        notes: Some("synthetic adjacent-bit delta control tensor".to_string()),
    }
}

fn read_fixture_dataset(fixture: &FixtureInput) -> Result<Dataset, String> {
    let values = read_f32le_file(&fixture.path)?;
    Ok(Dataset {
        group: fixture.group.clone(),
        name: fixture.name.clone(),
        source: DatasetSource::External,
        values,
        shape: fixture.shape.clone(),
        notes: fixture.notes.clone(),
    })
}

fn read_f32le_file(path: &Path) -> Result<Vec<f32>, String> {
    let byte_len = validate_f32le_file_metadata(path)?;
    let file = fs::File::open(path)
        .map_err(|error| format!("failed to read {}: {error}", path.display()))?;
    let mut reader = BufReader::new(file);
    let mut buffer = [0_u8; 64 * 1024];
    let mut pending = [0_u8; 4];
    let mut pending_len = 0_usize;
    let mut values = Vec::with_capacity(byte_len / 4);

    loop {
        let read = reader
            .read(&mut buffer)
            .map_err(|error| format!("failed to read {}: {error}", path.display()))?;
        if read == 0 {
            break;
        }
        let mut bytes = &buffer[..read];
        if pending_len > 0 {
            let needed = 4 - pending_len;
            if bytes.len() < needed {
                pending[pending_len..pending_len + bytes.len()].copy_from_slice(bytes);
                pending_len += bytes.len();
                continue;
            }
            pending[pending_len..4].copy_from_slice(&bytes[..needed]);
            values.push(f32::from_le_bytes(pending));
            bytes = &bytes[needed..];
        }
        let mut chunks = bytes.chunks_exact(4);
        for chunk in &mut chunks {
            values.push(f32::from_le_bytes(
                chunk.try_into().expect("chunk size checked"),
            ));
        }
        let remainder = chunks.remainder();
        pending[..remainder.len()].copy_from_slice(remainder);
        pending_len = remainder.len();
    }

    if pending_len != 0 {
        return Err(format!(
            "{} is not a raw f32le file: trailing partial f32",
            path.display()
        ));
    }
    Ok(values)
}

fn validate_f32le_file_metadata(path: &Path) -> Result<usize, String> {
    let metadata = fs::metadata(path)
        .map_err(|error| format!("failed to stat {}: {error}", path.display()))?;
    if !metadata.is_file() {
        return Err(format!("{} is not a file", path.display()));
    }
    let byte_len = usize::try_from(metadata.len())
        .map_err(|_| format!("{} is too large for this platform", path.display()))?;
    if byte_len % 4 != 0 {
        return Err(format!(
            "{} is not a raw f32le file: byte length {} is not divisible by 4",
            path.display(),
            byte_len
        ));
    }
    Ok(byte_len)
}

fn read_manifest_fixtures(path: &Path) -> Result<Vec<FixtureInput>, String> {
    let text = fs::read_to_string(path)
        .map_err(|error| format!("failed to read manifest {}: {error}", path.display()))?;
    let base_dir = path.parent().unwrap_or_else(|| Path::new("."));
    let mut fixtures = Vec::new();
    let mut current = ManifestFixture::default();
    let mut in_fixture = false;

    for (line_index, raw_line) in text.lines().enumerate() {
        let line_number = line_index + 1;
        let line = raw_line.split('#').next().unwrap_or("").trim();
        if line.is_empty() {
            continue;
        }
        if line == "[fixture]" {
            if in_fixture {
                fixtures.push(current.into_fixture_input(base_dir, path, line_number)?);
                current = ManifestFixture::default();
            }
            in_fixture = true;
            continue;
        }
        if !in_fixture {
            return Err(format!(
                "{}:{line_number}: expected [fixture] before key/value entries",
                path.display()
            ));
        }
        let (key, value) = line.split_once('=').ok_or_else(|| {
            format!(
                "{}:{line_number}: expected key = value entry",
                path.display()
            )
        })?;
        current.set(
            key.trim(),
            parse_manifest_value(value.trim()),
            path,
            line_number,
        )?;
    }

    if in_fixture {
        fixtures.push(current.into_fixture_input(base_dir, path, text.lines().count())?);
    }

    Ok(fixtures)
}

fn count_external_fixtures(benchmarked: &[BenchmarkedDataset]) -> usize {
    benchmarked
        .iter()
        .filter(|dataset| dataset.summary.source == DatasetSource::External)
        .count()
}

fn write_text_atomic(path: &Path, text: &str) -> Result<(), String> {
    let temp_path = temp_output_path(path);
    let result = (|| {
        let file = fs::File::create(&temp_path)
            .map_err(|error| format!("failed to create {}: {error}", temp_path.display()))?;
        let mut writer = BufWriter::new(file);
        writer
            .write_all(text.as_bytes())
            .map_err(|error| format!("failed to write {}: {error}", temp_path.display()))?;
        writer
            .flush()
            .map_err(|error| format!("failed to flush {}: {error}", temp_path.display()))?;
        fs::rename(&temp_path, path).map_err(|error| {
            format!(
                "failed to move {} to {}: {error}",
                temp_path.display(),
                path.display()
            )
        })
    })();
    if result.is_err() {
        let _ = fs::remove_file(&temp_path);
    }
    result
}

fn temp_output_path(path: &Path) -> PathBuf {
    let mut temp = path.as_os_str().to_owned();
    temp.push(format!(".tmp.{}", std::process::id()));
    PathBuf::from(temp)
}

struct GateResult {
    passed: bool,
    report: String,
}

fn evaluate_gate(benchmarked: &[BenchmarkedDataset], options: &BenchOptions) -> GateResult {
    let external_count = benchmarked
        .iter()
        .filter(|dataset| dataset.summary.source == DatasetSource::External)
        .count();
    let evaluate_external_only = external_count > 0;
    let mut passed = true;
    let mut rows = Vec::new();

    if options.gate_require_external && external_count == 0 {
        passed = false;
        rows.push(
            "| gate | external fixtures required | fail | no external fixtures were benchmarked |"
                .to_string(),
        );
    }

    for dataset in benchmarked {
        if evaluate_external_only && dataset.summary.source != DatasetSource::External {
            continue;
        }
        let Some(phase2) = find_result(&dataset.results, "phase2-lossless") else {
            passed = false;
            rows.push(format!(
                "| {} / {} | phase2-lossless row | fail | missing benchmark row |",
                dataset.summary.group, dataset.summary.name
            ));
            continue;
        };
        let mut dataset_passed = true;
        let mut reasons = Vec::new();
        if !phase2.exact_bits {
            dataset_passed = false;
            reasons.push("phase2 was not bit-identical".to_string());
        }
        if let Some(limit) = options.max_phase2_ratio {
            if phase2.ratio > limit {
                dataset_passed = false;
                reasons.push(format!("ratio {:.4} > {:.4}", phase2.ratio, limit));
            }
        }
        if let Some(limit) = options.max_phase2_encode_us {
            if phase2.encode_us > limit {
                dataset_passed = false;
                reasons.push(format!("encode {:.2}us > {:.2}us", phase2.encode_us, limit));
            }
        }
        if let Some(limit) = options.max_phase2_decode_us {
            if phase2.decode_us > limit {
                dataset_passed = false;
                reasons.push(format!("decode {:.2}us > {:.2}us", phase2.decode_us, limit));
            }
        }
        if !dataset_passed {
            passed = false;
        }
        rows.push(format!(
            "| {} / {} | phase2-lossless | {} | ratio {:.4}, encode {:.2}us, decode {:.2}us, exact_bits={}{} |",
            dataset.summary.group,
            dataset.summary.name,
            if dataset_passed { "pass" } else { "fail" },
            phase2.ratio,
            phase2.encode_us,
            phase2.decode_us,
            phase2.exact_bits,
            if reasons.is_empty() {
                String::new()
            } else {
                format!("; {}", reasons.join("; "))
            }
        ));

        let Some(container) = find_result(&dataset.results, "phase2-lossless-container") else {
            passed = false;
            rows.push(format!(
                "| {} / {} | phase2-lossless-container row | fail | missing benchmark row |",
                dataset.summary.group, dataset.summary.name
            ));
            continue;
        };
        let mut container_passed = true;
        let mut container_reasons = Vec::new();
        if !container.exact_bits {
            container_passed = false;
            container_reasons.push("container was not bit-identical".to_string());
        }
        if let Some(limit) = options.max_phase2_container_ratio {
            if container.ratio > limit {
                container_passed = false;
                container_reasons.push(format!("ratio {:.4} > {:.4}", container.ratio, limit));
            }
        }
        if let Some(limit) = options.max_phase2_container_decode_us {
            if container.decode_us > limit {
                container_passed = false;
                container_reasons.push(format!(
                    "decode {:.2}us > {:.2}us",
                    container.decode_us, limit
                ));
            }
        }
        if !container_passed {
            passed = false;
        }
        rows.push(format!(
            "| {} / {} | phase2-lossless-container | {} | ratio {:.4}, decode {:.2}us, exact_bits={}{} |",
            dataset.summary.group,
            dataset.summary.name,
            if container_passed { "pass" } else { "fail" },
            container.ratio,
            container.decode_us,
            container.exact_bits,
            if container_reasons.is_empty() {
                String::new()
            } else {
                format!("; {}", container_reasons.join("; "))
            }
        ));
    }

    let mut report = String::new();
    report.push_str("# Benchmark Gate\n\n");
    report.push_str(&format!(
        "- status: `{}`\n",
        if passed { "pass" } else { "fail" }
    ));
    report.push_str(&format!("- evaluated fixtures: `{}`\n", rows.len()));
    report.push_str(&format!("- external fixtures: `{external_count}`\n"));
    report.push_str(&format!(
        "- scope: `{}`\n",
        if evaluate_external_only {
            "external"
        } else {
            "synthetic"
        }
    ));
    report.push_str(&format!(
        "- max phase2 ratio: `{}`\n",
        option_f64_label(options.max_phase2_ratio)
    ));
    report.push_str(&format!(
        "- max phase2 encode us: `{}`\n",
        option_f64_label(options.max_phase2_encode_us)
    ));
    report.push_str(&format!(
        "- max phase2 decode us: `{}`\n\n",
        option_f64_label(options.max_phase2_decode_us)
    ));
    report.push_str(&format!(
        "- max phase2 container ratio: `{}`\n",
        option_f64_label(options.max_phase2_container_ratio)
    ));
    report.push_str(&format!(
        "- max phase2 container decode us: `{}`\n\n",
        option_f64_label(options.max_phase2_container_decode_us)
    ));
    report.push_str("| dataset | check | status | details |\n");
    report.push_str("| --- | --- | --- | --- |\n");
    for row in rows {
        report.push_str(&row);
        report.push('\n');
    }
    GateResult { passed, report }
}

fn option_f64_label(value: Option<f64>) -> String {
    value
        .map(|value| format!("{value:.4}"))
        .unwrap_or_else(|| "unset".to_string())
}

